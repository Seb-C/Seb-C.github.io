&lt;h1&gt;Déménagement du site&lt;/h1&gt;

&lt;p&gt;Vous avez peut-être remarqué que le site est resté inaccessible une dizaine de jours. J'ai en effet eu un souci avec la carte SD du Raspberry PI qui a lâchée. Ça tombe presque bien, car j'avais l'intention depuis quelque temps de revenir sur un hébergement &quot;normal&quot;, c'est à dire pas chez moi.&lt;/p&gt;

&lt;p&gt;C'est sympathique de l'héberger chez soi, mais ça pose aussi quelques contraintes et coups de stress au quotidien : quand il faut redémarrer sa box, couper le courant, déménager...&lt;/p&gt;

&lt;p&gt;Je voulais néanmoins une solution simplissime étant donné le peu de visiteurs que j'ai. J'ai donc opté pour un site purement statique, qui est héberge sur &lt;a href=&quot;https://pages.github.com/&quot;&gt;GitHub Pages&lt;/a&gt;. L'avantage, c'est que même mon site est &lt;a href=&quot;https://github.com/Seb-C/seb-c.github.io&quot;&gt;open source&lt;/a&gt;. L'inconvénient, c'est qu'il n'y a plus de commentaires possibles dans l'immédiat (et je ne souhaite pas adopter une solution extérieure comme Disqus). N'hésitez pas à me contacter par email malgré tout (le lien de contact est en bas de page).&lt;/p&gt;

&lt;p&gt;Si vous êtes abonné aux RSS, pensez à mettre à jour le lien !&lt;/p&gt;
